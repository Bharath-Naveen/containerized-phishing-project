def main() -> None:
    out_path = _REPO / "data" / "evaluation" / "simple_legit_urls.jsonl"
    pairs = _all_pairs()
    rows = _dedupe_rows(pairs)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as f:
        for r in rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    cats: Dict[str, int] = {}
    for r in rows:
        c = r.get("category", "")
        cats[c] = cats.get(c, 0) + 1
    print(f"Wrote {len(rows)} unique canonical URLs -> {out_path}")
    print("Top categories:", dict(sorted(cats.items(), key=lambda x: -x[1])[:15]))
    if len(rows) < 500:
        sys.exit(f"ERROR: only {len(rows)} rows; need >= 500")
    if len(rows) < 800:
        print(f"WARNING: only {len(rows)} rows; target band is 800–1000 for stronger influence.", file=sys.stderr)


if __name__ == "__main__":
    main()
