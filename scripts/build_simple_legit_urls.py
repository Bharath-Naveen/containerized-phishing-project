from src.pipeline.clean import canonicalize_url

OutRow = Tuple[str, str]  # url, category


def _dedupe_rows(pairs: List[OutRow]) -> List[Dict[str, str]]:
    seen: Set[str] = set()
    out: List[Dict[str, str]] = []
    for url, cat in pairs:
        url = url.strip()
        if not url:
            continue
        c, inv, err = canonicalize_url(url)
        if inv or not c:
            continue
        key = c.strip().lower()
        if key in seen:
            continue
        seen.add(key)
        out.append({"url": url, "category": cat})
    return out


